//boolean datatype
class Demo
{
	public static void main(String[] args)
	{
		boolean bl1; //declaration
		bl1=true;  //initialization
		boolean bl2=false; //declaration & initialization
		System.out.println(bl1); //use to display
		System.out.println(bl2);
		System.out.println(bl1+" "+bl2);
	}
}


/* steps to use variables
declare => datatype variablename;
initialize => variablename=value;
declare & initialize => datatype variablename=value;
use => to display => variablename */

