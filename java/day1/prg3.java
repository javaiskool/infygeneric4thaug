//character datatype
class Welcome
{
	public static void main(String[] args)
	{
		 
		char ch1='A',ch2='*',ch3;
		ch3='6';
		System.out.println(ch1+ch2+ch3); //uses ascii equivalent to perform addition => A = 65 * = 42 6 = 54 => 97+42+54 = 161
		System.out.print(ch1);
		System.out.print(ch2);
		System.out.println(ch3);
		System.out.println(ch1+""+ch2+""+ch3); //"" as seperator
		System.out.println(ch1+" "+ch2+" "+ch3); //" " 
		System.out.print(ch1+"\t"+ch2+"\t"+ch3+"\n"); // \t tab, \n new line
	}
}


 

