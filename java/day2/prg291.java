//demo of  switch case

class Demo
{
	public static void main(String[] s)
	{
		char ch='A';
		System.out.print("character type ");
		switch (ch)
		{
			case 'a':
			case 'A':
			case 'e':
			case 'E': 
			case 'i':
			case 'I':
			case 'o':
			case 'O':
			case 'u':
			case 'U':
				ystem.out.println("is vowel");
					break;
			 
			default : System.out.println(" is non-vowel");
			break;
			
		 }
	System.out.println("**************************************");
	}
	
}

