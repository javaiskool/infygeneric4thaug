//demo of  + operator in print

class Demo
{
	public static void main(String[] s)
	{
		int x=-90;
		boolean bl=true;
		char ch='A';
		String str="hello 10*20+70 ######";
		System.out.println(x+""+bl+""+ch+""+str);
		System.out.println(x+" "+bl+" "+ch+" "+str);
		System.out.println(x+"\t"+bl+"\t"+ch+"\t"+str);
		System.out.println(x+"\n"+bl+"\n"+ch+"\n"+str);

	}
}

